## 2. Amounts Owed / Utilization (30%)
### The "High Balance" Illusion

This is where 90% of people fail. They think "I pay my bill in full every month, so I''m good."
**WRONG.**

The credit bureaus do not see when you pay. They see what your balance is **on the Statement Closing Date**.

### The Statement Date vs. Due Date
- **Closing Date:** The day the bill prints. The bank takes a snapshot of your balance and sends it to the bureaus.
- **Due Date:** Usually 20-25 days *after* the Closing Date.

If you have a $1,000 limit and you spend $900, and the statement closes... the Bureau sees **90% utilization**. Your score tanks. Even if you pay it off 2 days later on the Due Date!

### The AZEO Method (All Zero Except One)
To get a perfect 850 score, you need **1-3% utilization**, not 30%.
1. Pay off every single card to $0 **before** the Statement Closing Date.
2. Leave **one** card with a tiny balance (like $10 or $20).
3. The bureaus see "0, 0, 0, 0, 0, $10".
4. This signals: "He uses credit, but he needs none of it."

**That is power.**

