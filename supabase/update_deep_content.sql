## 3. Length of Credit History (15%)
### The "Time Travel" Factor

You cannot rush time... or can you?
This factor averages the age of all your open accounts.

- Opening a new card **lowers** your average age.
- Closing an old card **removes** that history (eventually).

### Never Close Your First Card
Your oldest card is your anchor. Even if it has a crappy annual fee, downgrade it to a free version. Keep it open forever.

### Piggybacking (Authorized Users)
This is the cheat code.
If your grandmother has a generic Discover card open since 1985 with perfect payment history...
If she adds you as an **Authorized User**, her 40 years of history get copied and pasted onto YOUR report.
You instantly look like you have been managing credit since before you were born.

**Warning:** Only do this with people who have PERFECT execution. If they miss a payment, you miss a payment.

