## 1. Payment History (35%)
### The "Don''t Miss" Zone

This is the bedrock. 35% of your score is purely: *Did you pay on time?*

But here is the secret: **"On Time" in the real world means by the due date. "On Time" in the credit world means within 29 days of the due date.**

Banks cannot report you late to the bureaus until you are **30 days** past due.
- **Day 1 Late:** You pay a late fee. Internal record only.
- **Day 15 Late:** You get a phone call. Internal record only.
- **Day 29 Late:** You are sweating. Internal record only.
- **Day 30 Late:** **BOOM.** It hits your credit report. Score drops 50-100 points.

### The Strategy:
If you are struggling, you never, ever let it hit Day 30. You can negotiate a late fee reversal. You cannot easily negotiate a 30-day late marker removal (though we have methods for that in the Restoration module).

**Billionaire Move:** Autopay is for amateurs. Use **Credit Alignment**. Set all your credit card due dates to the 1st or the 15th of the month by calling the banks. Align your cash flow.

