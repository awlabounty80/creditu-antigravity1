## 4. New Credit (10%)
### The "Desperation" Signal

Every time you apply for credit, you get a **Hard Inquiry**.
- **Hard Pull:** You asked for money. (Score drops 3-5 points).
- **Soft Pull:** You checked your rate, or existing bank checked you. (Score unaffected).

Inquiries stay on your report for 2 years, but they only hurt your score for **12 months**.
If you have 10 inquiries in a month, you look desperate / broke. Banks hate desperate people.
If you have 0 inquiries in 2 years, you look stagnant.

**The Sweet Spot:** Apply for credit in "sprees." Apply for 3-4 cards on the same day. The inquiries merge or happen simultaneously before the new accounts hit the report.

---

## 5. Credit Mix (10%)
### The "Portfolio" Facade

Banks want to see that you can handle different *types* of debt.
- **Revolving Credit:** Credit Cards, Lines of Credit. (Variable payments).
- **Installment Loans:** Auto Loans, Mortgages, Student Loans. (Fixed payments).

If you only have credit cards, you are capped. You need an installment loan to break the 800 barrier securely.

### The Self-Lender Hack
Don''t go buy a car just to get a loan. Use a "Credit Builder Loan" (like Self or a Credit Union secured loan).
You lend money to *yourself*, pay yourself back, and the bank reports it as an installment loan.
It costs you a few dollars in interest, but it buys you the "Mix" badge in the algorithm.

